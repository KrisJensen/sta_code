# function for plotting filled-in circle arcs
def arc_patch(center, radius, theta1, theta2, ax=None, resolution=50, rotate = 0, **kwargs):
    # make sure ax is not empty
    if ax is None:
        ax = plt.gca()
    # generate the points
    theta = np.linspace(theta1, theta2, resolution) + rotate
    points = np.vstack((radius*np.cos(theta) + center[0],
                        radius*np.sin(theta) + center[1]))
    points = np.hstack([points, np.array(center)[:, None], points[:,:1]])
    # build the polygon and add it to the axes
    poly = mpatches.Polygon(points.T, closed=True, **kwargs)
    ax.add_patch(poly)
    return poly
